package dataStructures;

public class SinglyLinkedListNode {
	SinglyLinkedListNode next;
	int val;
	public SinglyLinkedListNode getNext() {
		return next;
	}
	public void setNext(SinglyLinkedListNode next) {
		this.next = next;
	}
	public int getVal() {
		return val;
	}
	public void setVal(int val) {
		this.val = val;
	}
	
	
	

}
